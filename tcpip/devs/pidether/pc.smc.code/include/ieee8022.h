/* Packet data definitions for LLC */
#define	S_SAP		0x00	/* NULL Source Service Access Point */
#define	D_SAP		0x00	/* NULL Destination Service Access Point */
#define	RESP_BIT	0x01	/* Response bit for SSAP */
#define	TST_CMND	0xE3	/* Defines a test command for the LLC */
#define	XID_CMND	0xAF	/* XID command */
#define	PF_BIT		0x10	/* the command poll/response final bit */

